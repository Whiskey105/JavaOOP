package Controller;

public enum Command {
    /**
     * enum для комманд, которые используются в работе программы
     *     NONE - пустое значение
     *     LOG - вывод лога операций
     *     SUM - сложение
     *     SUB - вычитание
     *     MUL - умножение
     *     DIV - деление
     *     HELP - вывод всех комманд
     *     EXIT - выход из приложения
     */
    NONE,
    LOG,
    SUM,
    SUB,
    MUL,
    DIV,
    HELP,
    EXIT
}
